#include <iostream>
using namespace std;

int main()
{	
 
	  
	char grade='A';
	switch(grade)
	{
	
	case 'A':
		cout<<"Excellent!"<<endl;
 		break;	
	case 'B':
		cout<<"Good!"<<endl;
		break;	
	case 'C':
		cout<<"Fair!"<<endl;
		break;
		
	case 'D':
		cout<<"Poor!"<<endl;
		break;
		
	otherwise :
		cout<<"Grade not valid!"<<endl;
		break;
	}
return 0;
}
